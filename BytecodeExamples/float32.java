class float32 {
    public static float add(float x, float y) {
        return x + y;
    }
    public static float sub(float x, float y) {
        return x - y;
    }
    public static float mult(float x, float y) {
        return x * y;
    }
    public static float div(float x, float y) {
        return x / y;
    }

    public static boolean eq(float x, float y) {
        return x == y;
    }
    public static boolean neq(float x, float y) {
        return x != y;
    }
    public static boolean lt(float x, float y) {
        return x < y;
    }
    public static boolean lteq(float x, float y) {
        return x <= y;
    }
    public static boolean gt(float x, float y) {
        return x > y;
    }
    public static boolean gteq(float x, float y) {
        return x >= y;
    }

}
