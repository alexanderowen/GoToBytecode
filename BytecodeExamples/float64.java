class float64 {
    public static double add(double x, double y) {
        return x + y;
    }
    public static double sub(double x, double y) {
        return x - y;
    }
    public static double mult(double x, double y) {
        return x * y;
    }
    public static double div(double x, double y) {
        return x / y;
    }

    public static boolean eq(double x, double y) {
        return x == y;
    }
    public static boolean neq(double x, double y) {
        return x != y;
    }
    public static boolean lt(double x, double y) {
        return x < y;
    }
    public static boolean lteq(double x, double y) {
        return x <= y;
    }
    public static boolean gt(double x, double y) {
        return x > y;
    }
    public static boolean gteq(double x, double y) {
        return x >= y;
    }

}
